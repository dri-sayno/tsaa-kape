                    The harvesting process begins the moment the coffee cherries are plucked from their trees. There are two methods of removing these cherries.<br>
                    The first is Strip Picking, in which the cherries of all stages of ripening are indiscriminately removed and taken for processing, the idea being to accumulate cherries in bulk for mass production.<br>
                    The second method - the one that Kape't Tsaá®; uses exclusively -- is Selective Picking. This is a more professional method that requires finesse and expertise as the picker evaluates and selects only those cherries deemed ready for processing. This way, unripe cherries are conserved for later picking, when they've reached their potential. By simply weighing the red ripe cherry fruit in their fingers, they can quickly and accurately judge its degree of ripeness, pluck it, and move on.<br>
