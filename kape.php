                    Before one of our delicious blends finds its way to your cup, it's first got to pass the test in one of ours.<br>
                    Cupping is the industry method of tasting a sample of coffee, scrutinizing it for the coffee's acidity, aroma, body and flavor. Each coffee is usually cupped with multiple samples to compare its quality and cross checked for consistency.<br>
                    The cupping process starts with adding boiling water to freshly ground coffee and letting it steep for 2-3 minutes. After clearing the floating coffee grounds, the next step is to taste coffee by taking a spoonful and slurping it rapidly, so that it fills all parts of the tongue. This allows the taster to sample the coffee's full profile, from aroma to flavor and finish, all in one rapid action.<br>
