                    Tea cupping or sensory evaluation is a process of utilizing human senses to appreciate and evaluate tea. Because quality can vary depending on its origin and the way tea is processed, tea buyers use this technique to determine quality and compare teas. Tea cupping is not just for tea professionals; tea enthusiasts often cup to deepen their appreciation for teas.</p>
                <p class="tea-cupping-text">                    
                    The first step is to prepare the tea brew. Measure 1 level teaspoon of fresh loose leaf tea into cupping set and cover with lid. In a kettle, heat cool filtered water to just under boil (185°F). Remove lid on cupping set and pour hot water into cup, filling to the top. Cover with lid and steep according to the recommended steeping time.</p>
                <p class="tea-cupping-text">
                    Once steeped to the appropriate time, carefully hold the lid down while pouring brewed tea into bowl. Once all the brew is drained, continue holding the cup and the lid together and tip the cup and lid upside down, shaking the leaves so they are on the inside of the lid. While the cup and the lid are upside down, remove the cup so that all the leaves are on the lid. Rest the lid filled with leaves on the cup.</p>
                <p class="tea-cupping-text">
                    To cup tea, you will need to your all your senses:<br>
                    <b>See:</b> Visually exam the dry and unfurled tea leaves. Whole leaf, twisted and rolled tea indicate quality. Also examine the color of tea leaves.<br>
                    <b>Smell:</b> Inhale the aroma of the tea and distinguish aromas of the tea. For example, earthiness, grassiness, citrus, floral and etc.<br>
                    <b>Taste:</b> With a stainless steel spoon, scoop up the tea brew and quickly slurp. Slurping coasts your tongue with the tea helping you evaluate all the flavors and mouth feel of the brew.<br>
                </p>
                <p class="tea-cupping-text">
                    The key to a successful sensory evaluation is consistency. Because individuals tend to develop personal preferences or methods, it is very important to consistently maintain the same procedures throughout the tea cupping session for all teas.<br>
